#include "libft.h"

void				ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*dst;

	i = 0;
	dst = (unsigned char *)s;
	if (n)
	{
		if (n != 0)
		{
			while (i < n)
			{
				dst[i] = 0;
				i++;
			}
		}
	}
}
