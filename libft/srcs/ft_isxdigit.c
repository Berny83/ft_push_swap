#include "libft.h"

int		ft_isxdigit(int c)
{
	if ((c >= 'A' && c <= 'F') || (c >= 'a' && c <= 'f'))
		return (c);
	else if (c >= '0' && c <= '9')
		return (c);
	else
		return (0);
}
